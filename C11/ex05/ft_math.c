#include "hd.h"

int	ft_add(int x, int y)
{
	return (x + y);
}

int	ft_subtract(int x, int y)
{
	return (x - y);
}

int	ft_multiply(int x, int y)
{
	return (x * y);
}

int	ft_divide(int x, int y)
{
	return (x / y);
}

int	ft_module(int x, int y)
{
	return (x % y);
}
